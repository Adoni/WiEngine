    class Preview extends SurfaceView implements SurfaceHolder.Callback {
        SurfaceHolder mHolder;
        Camera mCamera;
        int mCameraId;
        
        Preview(Context context) {
            super(context);
            
            // Install a SurfaceHolder.Callback so we get notified when the
            // underlying surface is created and destroyed.
            mHolder = getHolder();
            mHolder.addCallback(this);
            mHolder.setType(SurfaceHolder.SURFACE_TYPE_PUSH_BUFFERS);
        }

        public void surfaceCreated(SurfaceHolder holder) {
            // The Surface has been created, acquire the camera and tell it where
            // to draw.
        	if(mUseFront) {
        		if(Build.VERSION.SDK_INT >= 9) {
            		try {
            			// get number of camera
            			Method method = Camera.class.getMethod("getNumberOfCameras", new Class[0]);
            			int count = (Integer)method.invoke(Camera.class, new Object[0]);
            			
            			// iterate camera info to find front camera
            			int frontId = -1;
        				Class<?> cameraInfoClass = Class.forName("android.hardware.Camera$CameraInfo");
        				Object cameraInfo = cameraInfoClass.newInstance();
        				Field facingField = cameraInfoClass.getField("facing");
        				Field CAMERA_FACING_FRONT_field = cameraInfoClass.getField("CAMERA_FACING_FRONT");
        				int CAMERA_FACING_FRONT = CAMERA_FACING_FRONT_field.getInt(null);
        				method = Camera.class.getMethod("getCameraInfo", new Class[] { int.class, cameraInfoClass });
        				for(int i = 0; i < count; i++) {
        					method.invoke(Camera.class, new Object[] { i, cameraInfo });
        					int facing = facingField.getInt(cameraInfo);
        					if(facing == CAMERA_FACING_FRONT) {
        						frontId = i;
        						break;
        					}
        				}
        				
        				// if found, open it
        				if(frontId >= 0) {
        					mCameraId = frontId;
        					method = Camera.class.getMethod("open", new Class[] { int.class });
        					mCamera = (Camera)method.invoke(Camera.class, new Object[] { frontId });
        				}
        			} catch (Exception e) {
        			}
        		}
        	} else {
        		mCameraId = 0;
        		mCamera = Camera.open();
        	}
        	
        	// set preview if camera is opened
        	if(mCamera != null) {
                try {
                    mCamera.setPreviewDisplay(holder);
                 } catch (IOException exception) {
                     mCamera.release();
                     mCamera = null;
                 }
        	} else {
        		Toast.makeText(ImagePickerActivity.this, "Can't open camera", Toast.LENGTH_SHORT).show();
        		finish();
        	}
        }
        
        public void surfaceDestroyed(SurfaceHolder holder) {
            // Surface will be destroyed when we return, so stop the preview.
            // Because the CameraDevice object is not a shared resource, it's very
            // important to release it when the activity is paused.
        	mCamera.cancelAutoFocus();
            mCamera.stopPreview();
            mCamera.release();
            mCamera = null;
        }

        public void surfaceChanged(SurfaceHolder holder, int format, int w, int h) {
            // Now that the size is known, set up the camera parameters and begin
            // the preview.
            try {
	            Camera.Parameters parameters = mCamera.getParameters();
	            parameters.setPreviewSize(w, h);
	            mCamera.setParameters(parameters);
            } catch (Exception e) {
            	/*
            	 * XXX: in OPPO X903, set Parameter cause unknown crash
            	 */
            }
            
            // set correct rotation
            setCameraDisplayOrientation(mCameraId, mCamera);
            
            // start preview
            mCamera.startPreview();
            mCamera.autoFocus(mAutoFocusCallback);
        }
    }
    
